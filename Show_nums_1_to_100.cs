//Show numbers from 1-100
using System;

class Program
{
    static void Main()
    {
        Console.WriteLine("=== Show numbers from 1 until 100 ===");

        // Use repetition to display numbers
        for (int i = 1; i <= 100; i++)
        {
            Console.WriteLine(i);
        }

        // End of program
        Console.WriteLine("=== End of program ===");
    }
}
