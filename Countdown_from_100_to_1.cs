// Program to display numbers from 100 to 1
// Title: Countdown from 100 to 1
using System;

class Program
{
    static void Main()
    {
        Console.WriteLine("=== Countdown from 100 to 1 ===");

        // Loop to iterate from 100 to 1
        for (int i = 100; i >= 1; i--)
        {
            // Display the current number
            Console.WriteLine(i);
        }

        // End of the program
        Console.WriteLine("=== End of Program ===");
    }
}
